public class IfElseExample {
    public static void main(String[] args) {
        int a = 5;

        if (a > 5) {
            System.out.println("a > 5");
        } else if (a < 5) {
            System.out.println("a < 5");
        } else {
            System.out.println("a == 5");
        }
    }
}
